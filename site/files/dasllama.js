/* dasLLAMA page — renders straight from the profiling JSON in files/dasllama/.
   The JSON is the single source of truth (generated by the dasLLAMA profiling
   suite); all ratios, win counts and bars are derived here, never hand-placed. */

(function () {
  'use strict';

  var DATA = {};
  var LLM_ORDER = ['m1', 'zen2'];
  var BOX_LABEL = {
    m1: { name: 'Apple M1 Max', meta: '8 threads · macOS' },
    zen2: { name: 'Ryzen Threadripper 3990X', meta: '16 threads · Windows 11' }
  };

  function fmt(n, d) { return Number(n).toFixed(d == null ? 0 : d); }
  function tps(v) { return v > 0 ? fmt(v, 1) : '-'; }   // tok/s cell; '-' when unmeasured (matches gen_results cell_tps)
  // das / reference throughput ratio, or null when the @optional reference wasn't measured
  // (matches gen_results cell_ratio: both sides must be > 0). Null rows are excluded from
  // win counts, sorting, bars, and get a '-' cell — never a NaN.
  function ratio(das, ref) { return (ref > 0 && das > 0) ? das / ref : null; }

  // Map an LLM ratio to a 0–100 bar position, centered at 1.0× (50%),
  // one octave (2×) to full scale, in log space.
  function octPos(r) {
    var oct = Math.log2(r);
    oct = Math.max(-1, Math.min(1, oct));
    return 50 + oct * 50;
  }

  /* ── LLM scoreboard ────────────────────────────────────────── */
  function renderLLM() {
    var box = document.querySelector('.js-llm-box.is-active').dataset.box;
    var phase = document.querySelector('.js-llm-phase.is-active').dataset.phase; // prefill | emission
    var d = DATA['llm_' + box];
    var rows = d.tests.slice();

    var wins = 0, ties = 0, measured = 0;
    rows.forEach(function (t) {
      var r = ratio(t[phase].das_tok_s, t[phase].llama_cpp_tok_s);
      if (r === null) return;              // no reference on this box → not counted
      measured++;
      if (r > 1.005) wins++; else if (r >= 0.995) ties++;
    });

    // bars (sorted by ratio, descending; unmeasured rows sort last)
    function rval(t) { var r = ratio(t[phase].das_tok_s, t[phase].llama_cpp_tok_s); return r === null ? -Infinity : r; }
    var barRows = rows.slice().sort(function (a, b) { return rval(b) - rval(a); });
    var barsHTML = barRows.map(function (t) {
      var r = ratio(t[phase].das_tok_s, t[phase].llama_cpp_tok_s);
      if (r === null) {                    // no computable ratio → neutral bar, no NaN geometry
        return '<div class="dl-bar">' +
          '<div class="dl-bar__label">' + t.model + '</div>' +
          '<div class="dl-bar__track"><div class="dl-bar__center"></div></div>' +
          '<div class="dl-bar__val" style="color:var(--fg-faint)">-</div>' +
        '</div>';
      }
      var pos = octPos(r);
      var win = r >= 0.995;
      var left = Math.min(50, pos), width = Math.abs(pos - 50);
      return '' +
        '<div class="dl-bar ' + (win ? 'dl-bar--win' : 'dl-bar--loss') + '">' +
          '<div class="dl-bar__label">' + t.model + '</div>' +
          '<div class="dl-bar__track">' +
            '<div class="dl-bar__center"></div>' +
            '<div class="dl-bar__fill" style="left:' + left + '%;width:' + width + '%"></div>' +
          '</div>' +
          '<div class="dl-bar__val">' + fmt(r, 2) + '×</div>' +
        '</div>';
    }).join('');

    // full table
    function rcell(r) {
      return r === null
        ? '<td class="dl-num dl-dim">-</td>'
        : '<td class="dl-num ' + (r >= 0.995 ? 'dl-win' : 'dl-loss') + '">' + fmt(r, 2) + '×</td>';
    }
    var tableHTML = rows.map(function (t) {
      var pr = ratio(t.prefill.das_tok_s, t.prefill.llama_cpp_tok_s);
      var er = ratio(t.emission.das_tok_s, t.emission.llama_cpp_tok_s);
      return '<tr>' +
        '<td class="dl-td-model">' + t.model + '</td>' +
        '<td class="dl-num">' + fmt(t.active_b, 2) + '</td>' +
        '<td class="dl-num">' + fmt(t.prefill.das_tok_s, 1) + '</td>' +
        '<td class="dl-num dl-dim">' + tps(t.prefill.llama_cpp_tok_s) + '</td>' +
        rcell(pr) +
        '<td class="dl-num">' + fmt(t.emission.das_tok_s, 1) + '</td>' +
        '<td class="dl-num dl-dim">' + tps(t.emission.llama_cpp_tok_s) + '</td>' +
        rcell(er) +
        '</tr>';
    }).join('');

    var phaseLabel = phase === 'prefill' ? 'prefill · prompt processing' : 'decode · token generation';
    document.getElementById('llm-bars').innerHTML = barsHTML;
    document.getElementById('llm-caption').innerHTML =
      '<span class="dl-win">' + wins + ' / ' + measured + '</span> models faster' +
      (ties ? ' · ' + ties + ' tie' + (ties > 1 ? 's' : '') : '') +
      ' &nbsp;·&nbsp; ' + phaseLabel + ' &nbsp;·&nbsp; ' + BOX_LABEL[box].name;
    document.getElementById('llm-table-body').innerHTML = tableHTML;
    document.getElementById('llm-plat').textContent =
      d.platform.cpu.replace('64-Core Processor', '').trim() + ' · daslang ' + d.platform.daslang +
      ' · llama.cpp ' + d.platform.llama_cpp;
  }

  /* ── ASR scoreboard ────────────────────────────────────────── */
  var ASR_FEATURE = {
    m1: ['Parakeet-TDT v2', 'Whisper tiny'],
    zen2: ['Parakeet-TDT v2', 'Whisper large-v3-turbo']
  };

  function renderASR() {
    var box = document.querySelector('.js-asr-box.is-active').dataset.box;
    var d = DATA['asr_' + box];
    var wanted = ASR_FEATURE[box];

    var html = wanted.map(function (name) {
      var t = d.tests.find(function (x) { return x.model === name; });
      if (!t) return '';
      var refTool = t.references[0]; // primary cli reference
      // max speedup across buckets for bar scaling
      var maxSpeed = 0;
      t.buckets.forEach(function (b) {
        var ref = b.refs.find(function (r) { return r.tool === refTool; });
        if (ref) maxSpeed = Math.max(maxSpeed, ref.ms / b.das_ms);
      });
      var rowsHTML = t.buckets.map(function (b) {
        var ref = b.refs.find(function (r) { return r.tool === refTool; });
        if (!ref) return '';
        var speed = ref.ms / b.das_ms;      // >1 = das faster
        var xrt = (b.audio_s * 1000) / b.das_ms;
        var w = Math.max(4, (speed / maxSpeed) * 100);
        return '<div class="dl-asr-row">' +
          '<div class="dl-asr-row__file">' + b.wav + '<span class="dl-asr-row__dur">' + fmt(b.audio_s) + 's</span></div>' +
          '<div class="dl-asr-row__track"><div class="dl-asr-row__fill" style="width:' + w + '%"></div></div>' +
          '<div class="dl-asr-row__speed">' + fmt(speed, 2) + '× <span class="dl-asr-row__xrt">' + fmt(xrt) + '× RT</span></div>' +
          '</div>';
      }).join('');
      return '<div class="dl-asr-model">' +
        '<div class="dl-asr-model__head"><span class="dl-asr-model__name">' + t.model + '</span>' +
        '<span class="dl-asr-model__ref">das vs ' + refTool + '</span></div>' +
        rowsHTML + '</div>';
    }).join('');

    document.getElementById('asr-models').innerHTML = html;
    document.getElementById('asr-plat').textContent =
      d.platform.cpu.replace('64-Core Processor', '').trim() + ' · ' + d.platform.threads + ' threads';
  }

  /* ── Audio-chat blowout callouts (M1, the biggest wins) ─────── */
  function renderAudioChat() {
    var d = DATA.asr_m1;
    function best(model, wav) {
      var t = d.tests.find(function (x) { return x.model === model; });
      var b = t.buckets.find(function (x) { return x.wav === wav; });
      var ref = b.refs[0];
      return { speed: ref.ms / b.das_ms, das: b.das_ms, ref: ref.ms, audio: b.audio_s };
    }
    var q = best('Qwen3-Omni-30B audio', 'jfk.wav');
    var g = best('Gemma-4 E2B audio', 'gemma4a_test2.wav');
    var cards = [
      { n: fmt(q.speed, 1) + '×', t: 'Qwen3-Omni-30B, audio in', s: 'vs llama-mtmd-cli, CPU-only · ' + fmt(q.das) + ' ms vs ' + fmt(q.ref) + ' ms' },
      { n: fmt(g.speed, 1) + '×', t: 'Gemma-4 E2B, audio in', s: 'vs llama-mtmd-cli, CPU-only · ' + fmt(g.das) + ' ms vs ' + fmt(g.ref) + ' ms' }
    ];
    document.getElementById('audiochat-cards').innerHTML = cards.map(function (c) {
      return '<div class="dl-blowout"><div class="dl-blowout__n">' + c.n + '</div>' +
        '<div class="dl-blowout__t">' + c.t + '</div>' +
        '<div class="dl-blowout__s">' + c.s + '</div></div>';
    }).join('');
  }

  /* ── wiring ─────────────────────────────────────────────────── */
  function wireToggle(sel, cb) {
    document.querySelectorAll(sel).forEach(function (btn) {
      btn.addEventListener('click', function () {
        var group = btn.parentElement;
        group.querySelectorAll(sel).forEach(function (b) { b.classList.toggle('is-active', b === btn); });
        cb();
      });
    });
  }

  function boot() {
    renderLLM();
    renderASR();
    renderAudioChat();
    wireToggle('.js-llm-box', renderLLM);
    wireToggle('.js-llm-phase', renderLLM);
    wireToggle('.js-asr-box', renderASR);
  }

  Promise.all([
    fetch('files/dasllama/profile_llm_m1.json').then(function (r) { return r.json(); }),
    fetch('files/dasllama/profile_llm_zen2.json').then(function (r) { return r.json(); }),
    fetch('files/dasllama/profile_asr_m1.json').then(function (r) { return r.json(); }),
    fetch('files/dasllama/profile_asr_zen2.json').then(function (r) { return r.json(); })
  ]).then(function (res) {
    DATA.llm_m1 = res[0]; DATA.llm_zen2 = res[1];
    DATA.asr_m1 = res[2]; DATA.asr_zen2 = res[3];
    boot();
  }).catch(function (e) {
    document.getElementById('llm-bars').innerHTML =
      '<div style="color:var(--red);font-family:var(--font-mono);font-size:13px">Could not load profiling data: ' + e + '</div>';
  });
})();
